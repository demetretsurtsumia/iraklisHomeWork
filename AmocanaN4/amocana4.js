/* 4. კიბეზე ასვლის გზები
ამოცანა: დაწერეთ რეკურსიული ფუნქცია, რომელიც გამოთვლის n საფეხურიან კიბეზე ასვლის გზების რაოდენობას, თუ ერთი ნაბიჯით შეგიძლიათ გადახვიდეთ 1, 2 ან 3 საფეხურს.
მაგალითი:  count_ways(4) → 7 (1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2, 1+3, 3+1)*/

function count_ways(n) {

    if (n === 0) return 1;
    if (n < 0) return 0;  

  
    return count_ways(n - 1) + count_ways(n - 2) + count_ways(n - 3);
}
